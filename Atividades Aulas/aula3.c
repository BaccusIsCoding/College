#include <stdio.h>
#include <stdlib.h>

int main()
{
    //declaração de caracteres:
    
    float cota, valor_r, valor_d;

    //atribuição de um caractere:
    cota=4.60;

    printf("\nA cotação do dólar de hoje é: %.2f\n", cota);

    printf("\nDigite o valor a ser convertido: U$");
    
    scanf("%f",&valor_d);
    
    //transformação de uma variável através de uma expressão
    valor_r=cota*valor_d;
    printf("\nU$%.2f Vale hoje R$%.2f\n", valor_d, valor_r);

    return 0;
}
